def sudoku_check(grid):
    for row in range(len(grid)):
        for col in range(len(grid)):
            if grid[row][col] > len(grid):
                return False

    for row in grid:
        if sorted(list(set(row))) != sorted(row):
            return False

    cols = []
    for col in range(len(grid)):
        for row in grid:
            cols += [row[col]]

        if sorted(list(set(cols))) != sorted(cols):
            return False
        cols = []

    return True


